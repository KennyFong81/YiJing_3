# ====================== 時辰 & 五行函數（不變）======================
def get_hour_number(hour):
    """嚴格按照你指定的時辰數目"""
    if 0 <= hour < 2:      # 凌晨12點～凌晨兩點
        return 1
    elif 2 <= hour < 4:    # 凌晨兩點～四點
        return 2
    elif 4 <= hour < 6:
        return 3
    elif 6 <= hour < 8:
        return 4
    elif 8 <= hour < 10:
        return 5
    elif 10 <= hour < 12:
        return 6
    elif 12 <= hour < 14:
        return 7
    elif 14 <= hour < 16:
        return 8
    elif 16 <= hour < 18:
        return 9
    elif 18 <= hour < 20:
        return 10
    elif 20 <= hour < 22:
        return 11
    else:                  # 22點～24點
        return 12

